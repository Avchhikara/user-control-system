const mongoose = require("mongoose");
const Schema = mongoose.Schema,
  model = mongoose.model;

const userSchema = new Schema({
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

module.exports = mongoose.models.User || model("User", userSchema);
